    public static String formatDate(String format, Date date) {
        String result = null;
        if (format != null && date != null) {
            SimpleDateFormat fmt = new SimpleDateFormat(format);
            try {
                result = fmt.format(date);
            } catch (Exception e) {
                logger.error("解析日期异常", e);
            }
        }

        return result;
    }

