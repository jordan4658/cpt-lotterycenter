    public static String splitAndRandom(String source) {
        if (org.apache.commons.lang3.StringUtils.isNotEmpty(source)) {
            String[] tempArray = source.split(",");
            String temp = tempArray[((int) (Math.random() * 10000000D)) % tempArray.length];
            if (temp != null && temp.trim().length() > 0) {
                return temp.trim();
            }
        }
        return null;
    }

