package com.caipiao.core.library.vo.lotterymanage;

public class LotteryCategoryVO {
	
	
	private Integer id ;
	
	private String name;
	
	private String alias;
	 
	private String playTable;
	
	private Integer startId;
	
	private Integer numberOddsStatus;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAlias() {
		return alias;
	}

	public void setAlias(String alias) {
		this.alias = alias;
	}

	public String getPlayTable() {
		return playTable;
	}

	public void setPlayTable(String playTable) {
		this.playTable = playTable;
	}

	public Integer getStartId() {
		return startId;
	}

	public void setStartId(Integer startId) {
		this.startId = startId;
	}

	public Integer getNumberOddsStatus() {
		return numberOddsStatus;
	}

	public void setNumberOddsStatus(Integer numberOddsStatus) {
		this.numberOddsStatus = numberOddsStatus;
	}
	
	
	
	
}
